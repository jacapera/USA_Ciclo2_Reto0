public class Multiplicacion {
    private double num1;
    private double num2;

    public Multiplicacion(double num1, double num2) {
        this.num1 = num1;
        this.num2 = num2;
    }
    
    public double multiplicar(double num1, double num2){
        return num1 * num2;
    }
}
